
public class Cat {
	private String name;
	private String address;
	public Cat(String _name, String _address) {
		name = _name;
		address = _address;
	}
	public void setName(String name) {
		this.name = name;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getName() {
		return name;
	}
	public String getAddress() {
		return address;
	}
}
